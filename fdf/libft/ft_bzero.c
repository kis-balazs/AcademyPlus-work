#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s_char;

	if (s == NULL || n <= 0)
		return ;
	s_char = (unsigned char *)s;
	while (n--)
		*s_char++ = 0;
}
