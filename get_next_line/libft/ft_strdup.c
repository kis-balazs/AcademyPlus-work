#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*copy;
	int		i;

	if (s1 == NULL)
		return (NULL);
	i = 0;
	copy = (char *)malloc(ft_strlen(s1) + 1);
	if (copy == NULL)
		return (NULL);
	while (s1[i])
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
