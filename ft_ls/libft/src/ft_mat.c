int				ft_pow(int n, int p)
{
	int tmp;

	if (p)
	{
		tmp = n;
		while (p - 1)
		{
			n = n * tmp;
			p--;
		}
		return (n);
	}
	else
		return (1);
}

unsigned int	ft_abs(signed int n)
{
	return (n < 0 ? n * -1 : n);
}
