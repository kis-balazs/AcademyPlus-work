#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int	s_s1;

	s_s1 = ft_strlen(s1);
	ft_strncpy((s1 + ft_strlen(s1)), s2, n);
	s1[s_s1 + n] = 0;
	return (s1);
}
